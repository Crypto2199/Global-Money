export function tr(key, ...args) {
  const dict = TRANSLATIONS[state.lang] || TRANSLATIONS.ar;
  const v = dict[key];
  return typeof v === "function" ? v(...args) : (v ?? key);
}

export function applyLang() {
  const lang = state.lang;
  const isAr = lang === "ar";
  document.documentElement.lang = lang;
  document.documentElement.dir  = isAr ? "rtl" : "ltr";
  document.body.style.fontFamily = isAr
    ? "'Cairo','Segoe UI',sans-serif"
    : "'Inter','Segoe UI',sans-serif";

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.ar;
    const v = dict[el.dataset.i18n];
    // Use textContent instead of innerHTML where no HTML tags needed
    if (v !== undefined && typeof v !== "function") {
      el.textContent = v;
    }
  });

  document.querySelectorAll(".cur-search-input").forEach(i => {
    i.placeholder = tr("searchPlaceholder");
  });
  document.querySelectorAll(".lang-btn").forEach(b => {
    b.classList.toggle("active", b.dataset.lang === lang);
  });
}
